// IPA 公式「解答例」(officialAnswers.ts) には解説が無いため、Claude が問題本文 PDF を
// 精読して根拠付きの独自解説を付与する。IPA 引用データ (officialAnswers.ts) とは分離し、
// 本ファイルは汚染しない。設計書: docs/afternoon_explanation_design.md §4 / detailed_design §2.7g
//
// 著作権: 本文の逐語引用は禁止。basis は位置参照＋言い換えに留める。

/** 解答行ごとの解説（officialAnswers の AnswerRow と rowKey で 1:1 対応） */
export interface AfternoonRowExplanation {
  /** `${s}|${q ?? ''}|${t ?? ''}` で officialAnswers の行と対応 */
  rowKey: string
  /** この設問が問う力点（30-60字目安） */
  point: string
  /** 本文中の根拠（位置参照＋言い換え。逐語引用しない。40-90字目安） */
  basis: string
  /** なぜこの解答例になるか（60-120字目安） */
  reasoning: string
  /** ありがちな失点・誤答パターン（難所のみ任意。40-80字目安） */
  pitfall?: string
}

/** 設問ごとの詳細解説（詳細解説ページ用。深掘り学習向け） */
export interface AfternoonQuestionDetail {
  /** `${s}|${q ?? ''}|${t ?? ''}` で officialAnswers の行と対応 */
  rowKey: string
  /** 表示用の設問見出し（例「設問1(1)」） */
  heading: string
  /** この設問が問うていること（設問文の要点・字数条件を含めやや詳しく） */
  asked: string
  /** 解答に至る考え方のプロセス（ステップ列） */
  thinkingProcess: string[]
  /** 解答例（officialAnswers と一致。説明のため再掲） */
  modelAnswer: string
  /** 詳細解説（根拠・なぜその解答か・部分点や失点の機微） */
  commentary: string
}

/** その問題で習得すべき知識（用語＋説明） */
export interface AfternoonKeyKnowledge {
  term: string
  description: string
}

/** 問題文をセクション単位で解説する1ブロック */
export interface AfternoonProblemSection {
  /** 本文のセクション名・段落見出し（例「[状況の分析と対策]」「冒頭：事業の背景と課題」） */
  heading: string
  /** そのセクションの解説（==赤==/__navy__ マークアップ可・段落区切りは \n）。関連設問への言及も可 */
  body: string
}

/** 図表のアクセント配色（関係図ノード／比較表の強調列に使用） */
export type AfternoonFigureAccent =
  | 'brand'
  | 'indigo'
  | 'emerald'
  | 'amber'
  | 'rose'
  | 'slate'

/** 比較表の1行（観点＋各列の値） */
export interface AfternoonCompareRow {
  /** 観点（左端の見出し列。MarkupText 可） */
  label: string
  /** 各列の値（columns[1..] に対応。MarkupText 可） */
  cells: string[]
}

/**
 * 関係図のノード。グリッド座標（col,row）で配置する。
 * モバイル可読性のため col は 0〜1 を目安に（=最大2列幅）。長さは row で稼ぐ。
 * col/row は小数可（例 col:0.5 で中央寄せの三角配置）。
 */
export interface AfternoonDiagramNode {
  id: string
  /** 表示ラベル（改行は \n。1〜3行・短語推奨） */
  label: string
  /** グリッド列（0始まり・小数可） */
  col: number
  /** グリッド行（0始まり・小数可） */
  row: number
  /** 配色アクセント（既定 slate） */
  accent?: AfternoonFigureAccent
}

/** 関係図のエッジ（ノード間の矢印） */
export interface AfternoonDiagramEdge {
  from: string
  to: string
  /** 矢印に添えるラベル（短語） */
  label?: string
  /** 破線にする */
  dashed?: boolean
  /** 双方向矢印にする */
  bidirectional?: boolean
}

/** 図表（比較表 or 関係図）。詳細解説ページの problemSections の後に表示する */
export type AfternoonFigure =
  | {
      kind: 'compare'
      /** 図表タイトル */
      title: string
      /** 補足（表の下に小さく表示。MarkupText 可） */
      note?: string
      /** 列見出し。columns[0] は観点列の見出し（例「観点」「段階」） */
      columns: string[]
      /** 行（観点＋各列の値） */
      rows: AfternoonCompareRow[]
      /** 強調する列の index（columns 基準。例 [2] でその列を色付け） */
      highlightCols?: number[]
    }
  | {
      kind: 'diagram'
      /** 図表タイトル */
      title: string
      /** 補足（図の下に小さく表示。MarkupText 可） */
      note?: string
      nodes: AfternoonDiagramNode[]
      edges?: AfternoonDiagramEdge[]
    }

/** 詳細解説（深掘りページ用。任意。未投入なら詳細ページ非表示） */
export interface AfternoonDetailedExplanation {
  /** 問題文をセクションごとに紐解く解説 */
  problemSections: AfternoonProblemSection[]
  /** 図表（比較表・関係図）。任意。problemSections の後・設問別解説の前に表示 */
  figures?: AfternoonFigure[]
  /** 各設問の詳細解説＋考え方のプロセス */
  questionDetails: AfternoonQuestionDetail[]
  /** この問題で習得すべき知識 */
  keyKnowledge: AfternoonKeyKnowledge[]
  /** 応用できる解法の型（午後I全体に転用できる汎用テクニック。任意。MarkupText 可） */
  solvingTips?: string[]
}

/** 1問分の解説（officialAnswers の 1 OfficialAnswerSet に対応） */
export interface AfternoonExplanation {
  /** officialAnswers.id と一致（例 'R6-PM1-1'） */
  id: string
  /** 問題全体の趣旨・題材・問われる PMBOK 観点（100-200字目安。checkMode 概要） */
  overview: string
  /** 行ごとの解説（officialAnswers.answers と対応。checkMode アコーディオン） */
  rows: AfternoonRowExplanation[]
  /** 詳細解説（深掘りページ用。任意） */
  detail?: AfternoonDetailedExplanation
}

